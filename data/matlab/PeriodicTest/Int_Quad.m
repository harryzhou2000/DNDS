function r = Int_Quad(r, f,coords)

pGauss = [
    -0.7745966692414833, 0, 0.7745966692414833, -0.7745966692414833, 0, 0.7745966692414833, -0.7745966692414833, 0, 0.7745966692414833
    -0.7745966692414833, -0.7745966692414833, -0.7745966692414833, 0, 0, 0, 0.7745966692414833, 0.7745966692414833, 0.7745966692414833];
wGauss = [
    25. / 81., 40. / 81., 25. / 81., 40. / 81., 64. / 81., 40. / 81., 25. / 81., 40. / 81., 25. / 81.];


for i = 1:numel(wGauss)
    DiNj = quadDiNj(pGauss(:,i));
    pPhy =  coords * DiNj(1,:)';
    JM = coords * DiNj(2:3,:)';
    
    
    
    r = r + f(pPhy, pGauss(i), i) * wGauss(i) * det(JM);
end


    function DiNj = quadDiNj(p)
        DiNj = nan(3,4); %i: 0, dx, dy
        DiNj(1,1) = (1-p(1)) * (1-p(2)) / 4;
        DiNj(1,2) = (1+p(1)) * (1-p(2)) / 4;
        DiNj(1,3) = (1+p(1)) * (1+p(2)) / 4;
        DiNj(1,4) = (1-p(1)) * (1+p(2)) / 4;
        
        DiNj(2,1) = (-1) * (1-p(2)) / 4;
        DiNj(2,2) = (1) * (1-p(2)) / 4;
        DiNj(2,3) = (1) * (1+p(2)) / 4;
        DiNj(2,4) = (-1) * (1+p(2)) / 4;
        
        DiNj(3,1) = (1-p(1)) * (-1) / 4;
        DiNj(3,2) = (1+p(1)) * (-1) / 4;
        DiNj(3,3) = (1+p(1)) * (1) / 4;
        DiNj(3,4) = (1-p(1)) * (1) / 4;
        
    end



end